    private void btnCadastarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastarActionPerformed
        Valida v = new Valida();
       Integer resp = v.validaNome(txtNome.getText());
       if(resp != 0){
           
           conn = Conexao.conecta();
        
        try {
            if(conn == null || conn.isClosed()){
                JOptionPane.showMessageDialog(null,"erro ao conectar ao banco de dados!");
                System.exit(-1);
            }
            
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
                Date aux = Date.from(Instant.now());
                String nome = "'"+txtNome.getText()+"'";
                String perfil = ",'"+getCbPerfil().getSelectedItem().toString()+"'";
                String dataCad = ",'"+sdf.format(aux)+"'";

                String sql = "INSERT INTO tbUsuario (nome, perfil, data_cad) VALUES "
                        + "("+nome+perfil+dataCad+")";
                //JOptionPane.showMessageDialog(null,"sql: "+sql);

                Statement stmt = conn.createStatement();
                
                Integer retorno = stmt.executeUpdate(sql);
                
                if(retorno != 0){
                    JOptionPane.showMessageDialog(null,"Usu√°rio cadastrado com sucesso!","CADASTRO",JOptionPane.INFORMATION_MESSAGE);
                    
                }else{
                    JOptionPane.showMessageDialog(null,"N√£o foi possivel cadastar!","CADASTRO",JOptionPane.ERROR_MESSAGE);
                }
                
                
                
                stmt.close();
                conn.close();
                
                listagemUser();
                
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
           
       }
      
 
    }//GEN-LAST:event_btnCadastarActionPerformed

