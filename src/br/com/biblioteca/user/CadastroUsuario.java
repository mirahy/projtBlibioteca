    void setDados(String  id){
    btnCadastar.setEnabled(false);
    btnAlterar.setEnabled(true);
    btnExcluir.setEnabled(true);
    conn = Conexao.conecta();
    
    try {
            if(conn == null || conn.isClosed()){
                JOptionPane.showMessageDialog(null,"Erro ao conectar ao banco de dados!");
                System.exit(-1);
            }
                Integer cond = Integer.parseInt(id);

                String sql = "SELECT idtbUsuario, nome, perfil, data_cad from tbUsuario where idtbUsuario = '" + cond + "'";
        
        try ( 
                Statement stmt = conn.createStatement()) {
                //select
                ResultSet res = stmt.executeQuery(sql);
            
                    while(res.next()){
                        
                    
                    u.setId(res.getInt("idtbUsuario"));
                    u.setPerfil(res.getString("perfil"));
                    
                    txtNome.setText(res.getString("nome"));
                    CbPerfil.getModel().setSelectedItem(u.getPerfil());

                    
                }
                stmt.close();
            }
        
                conn.close();
                
            } catch (SQLException ex) {
                Logger.getLogger(ListagemLivros.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
    
