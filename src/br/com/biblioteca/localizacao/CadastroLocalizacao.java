    void setDados(String  id){
    btnCadastar.setEnabled(false);
    btnAlterar.setEnabled(true);
    btnExcluir.setEnabled(true);
    conn = Conexao.conecta();
    
    try {
            if(conn == null || conn.isClosed()){
                JOptionPane.showMessageDialog(null,"Erro ao conectar ao banco de dados!");
                System.exit(-1);
            }
                Integer cond = Integer.parseInt(id);

                String sql = "SELECT idtbLocalizacao, estante, prateleira, coluna from tbLocalizacao where idtbLocalizacao = '" + cond + "'";
        
        try ( 
                Statement stmt = conn.createStatement()) {
                //select
                ResultSet res = stmt.executeQuery(sql);
            
                    while(res.next()){
                        
                    
                    l.setId(res.getInt("idtbLocalizacao"));
                    
                    txtEstante.setText(res.getString("estante"));
                    txtPrateleira.setText(res.getString("prateleira"));
                    txtColuna.setText(res.getString("coluna"));
                    
                    

                    
                }
                stmt.close();
            }
        
                conn.close();
                
            } catch (SQLException ex) {
                Logger.getLogger(ListagemLivros.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
    
