    private void btnCadastarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastarActionPerformed
        Valida v = new Valida();
       Integer resp = v.validaLocalizacao(txtEstante.getText()+txtPrateleira.getText()+txtColuna.getText());
       if(resp != 0){
           
           conn = Conexao.conecta();
        
        try {
            if(conn == null || conn.isClosed()){
                JOptionPane.showMessageDialog(null,"erro ao conectar ao banco de dados!");
                System.exit(-1);
            }
            
                
                String estante = "'"+txtEstante.getText()+"'";
                String prateleira = ",'"+txtPrateleira.getText()+"'";
                String coluna = ",'"+txtColuna.getText()+"'";
                

                String sql = "INSERT INTO tbLocalizacao (estante, prateleira, coluna) VALUES "
                        + "("+estante+prateleira+coluna+")";
                //JOptionPane.showMessageDialog(null,"sql: "+sql);

                Statement stmt = conn.createStatement();
                
                Integer retorno = stmt.executeUpdate(sql);
                
                if(retorno != 0){
                    JOptionPane.showMessageDialog(null,"Localização cadastrada com sucesso!","CADASTRO",JOptionPane.INFORMATION_MESSAGE);
                    
                }else{
                    JOptionPane.showMessageDialog(null,"Não foi possivel cadastar!","CADASTRO",JOptionPane.ERROR_MESSAGE);
                }
                
                
                
                stmt.close();
                conn.close();
                
                listagemUser();
                
        } catch (SQLException ex) {
            Logger.getLogger(CadastroLocalizacao.class.getName()).log(Level.SEVERE, null, ex);
        }
           
       }
      
 
    }//GEN-LAST:event_btnCadastarActionPerformed

