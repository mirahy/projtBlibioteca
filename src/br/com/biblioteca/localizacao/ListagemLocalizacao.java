    public ListagemLocalizacao() throws ClassNotFoundException {
        initComponents();
        setLocationRelativeTo(null);
       tabelalocalizacao.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
       tabelalocalizacao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        Localizacao = new ArrayList<>();
        
        conn = Conexao.conecta();
        
        
        try {
            if(conn == null || conn.isClosed()){
                JOptionPane.showMessageDialog(null,"Erro ao conectar ao banco de dados!");
                System.exit(-1);
            }
            
                String sql = "SELECT idtbLocalizacao, estante, prateleira, coluna from tbLocalizacao";
                
                //JOptionPane.showMessageDialog(null,"sql: "+sql);

                Statement stmt = conn.createStatement();
                
                //select
                ResultSet res = stmt.executeQuery(sql);
                
//                if(res.getBoolean(ERROR)){
//                
//                    JOptionPane.showMessageDialog(null,"NÃ£o foi possivel cadastar!","CADASTRO",JOptionPane.ERROR_MESSAGE);
//            }
                
                while(res.next()){
//                    System.out.println("id: " + res.getInt("id"));
//                    System.out.println("Nome: " + res.getString("nome"));
//                    System.out.println("Data de nascimento: " + res.getDate("data_nascimento"));
                    
                    Localizacao lo = new Localizacao();
                    lo.setId(res.getInt("idtbLocalizacao"));
                    lo.setEstante(res.getString("estante"));
                    lo.setPrateleira(res.getString("prateleira"));
                    lo.setColuna(res.getString("coluna"));
                    

                    Localizacao.add(lo);
                }
                
                LocalizacaoTableMobel modelo = new LocalizacaoTableMobel();
                modelo.setListaLocalizacao(Localizacao);
                
                tabelalocalizacao.setModel(modelo);
                
                //insert, update, delete
               //stmt.executeUpdate(sql);
                
                stmt.close();
                conn.close();
                
        } catch (SQLException ex) {
            Logger.getLogger(ListagemLocalizacao.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tabelalocalizacao.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent e) {
                if(e.getClickCount() == 2) {
                    int row = tabelalocalizacao.getSelectedRow();
                    String id = String.valueOf(tabelalocalizacao.getValueAt(row, 0));
                    CadastroLocalizacao cl = new CadastroLocalizacao();
                    cl.setDados(id);
                    cl.setVisible(true);
                    setVisible(false);
                }
            }
            
        });
        
        
    }

